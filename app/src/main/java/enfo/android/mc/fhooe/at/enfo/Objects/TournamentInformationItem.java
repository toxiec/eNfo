public class TournamentInformationItem {

    private String mHeadLine;
    private String mText;

    public TournamentInformationItem(String _headline, String _text){
        mHeadLine = _headline;
        mText = _text;
    }

    public String getmHeadLine() {
        return mHeadLine;
    }

    public String getmText() {
        return mText;
    }


}
